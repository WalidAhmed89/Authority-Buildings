    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        if (Report_id_Text.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Select The Report You Need to Manage");
        } else {
            try {
                //Connection
                Connection c = Connection_fun.con();

                // Get the list model from Spare_Parts_List_Total
                ListModel<String> model = Spare_Parts_List_Total.getModel();

                // Concatenate all items into a single string, separated by commas
                StringBuilder concatenatedSpareParts = new StringBuilder();

                for (int i = 0; i < model.getSize(); i++) {
                    String item = model.getElementAt(i);
                    concatenatedSpareParts.append(item);

                    // Add a comma if it's not the last item
                    if (i < model.getSize() - 1) {
                        concatenatedSpareParts.append(", ");
                    }
                }

                // Prepare the SQL statement to insert the concatenated string into the CLOB column
                PreparedStatement ListSpare = c.prepareStatement("INSERT INTO Sub_Total VALUES(Sub_Total_SEQ.NEXTVAL,?,?,?)");

                // Set the concatenated string as the value for the CLOB
                ListSpare.setString(1, Report_id_Text.getText());
                ListSpare.setString(2, concatenatedSpareParts.toString());
                ListSpare.setString(3, Total.getText());

                // Execute the insertion
                ListSpare.executeQuery();

                //Get the Sub_Total With the Report_id Selection
                String ListSpare_id = null;
                PreparedStatement Sub_Total_id = c.prepareStatement("Select ID from Sub_Total Where Report_id = ?");
                Sub_Total_id.setString(1, Report_id_Text.getText());
                ResultSet Sub_Total_Result = Sub_Total_id.executeQuery();
                if (Sub_Total_Result.next()) {
                    ListSpare_id = Sub_Total_Result.getString("ID");
                } else {
                    JOptionPane.showMessageDialog(this, "Sub Total ID Name not found.");
                }

                //Get The Technical ID With the Name Selection.
                String Technical_Name_id = null;
                PreparedStatement Technical_id = c.prepareStatement("SELECT ID FROM EmployeesPosition WHERE First_Name || ' ' || Last_Name =? AND Employee_type = 'Technical'");
                Technical_id.setObject(1, Technical_Name.getSelectedItem());
                ResultSet Technical_id_Result = Technical_id.executeQuery();
                if (Technical_id_Result.next()) {
                    Technical_Name_id = Technical_id_Result.getString("ID");
                } else {
                    JOptionPane.showMessageDialog(this, "Technical Name not found.");
                }

                // Retrieve Location ID
                String Location_id = null;
                PreparedStatement Location_id_statement = c.prepareStatement("SELECT id FROM location WHERE role = ? AND office_number = ?");
                Location_id_statement.setString(1, Floor.getText());
                Location_id_statement.setString(2, Office.getText());
                ResultSet Location_id_result = Location_id_statement.executeQuery();

                if (Location_id_result.next()) {
                    Location_id = Location_id_result.getString("id");
                } else {
                    JOptionPane.showMessageDialog(this, "Location not found.");
                }

                //Set the data from Technical
                PreparedStatement Update_Reports_statement = c.prepareStatement("UPDATE Reports SET EndDate =sysdate,Condition_ID =3,Technical_id=?,Sub_Total_id=? WHERE ID =?");
                Update_Reports_statement.setInt(1, Integer.parseInt(Technical_Name_id));
                Update_Reports_statement.setInt(2, Integer.parseInt(ListSpare_id));
                Update_Reports_statement.setInt(3, Integer.parseInt(Report_id_Text.getText()));
                int updateCount = Update_Reports_statement.executeUpdate();
                if (updateCount >= 1) {
                    JOptionPane.showMessageDialog(this, "Success");
                    Report_id_Text.setText("");
                    Floor.setText("");
                    Office.setText("");
                    ((DefaultListModel) Spare_Parts_List_Total.getModel()).clear();
                    Table.clearSelection();
                    Cost.setText("");
                    TotalCost = 0;
                    Total.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "We Have a issue ");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Technical_MaintenanceInformation.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_SubmitActionPerformed


    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            //Connection
            Connection c = Connection_fun.con();
            Statement statement = c.createStatement();

            // put the Technical Name in the combobox
            new Get_SQL_Data_ComboBox(
                    "select count(First_name) from EMPLOYEESPOSITION where employee_type = 'Technical'",
                    "select First_name || ' ' || Last_Name as Technical_Name from EMPLOYEESPOSITION where employee_type = 'Technical'",
                    "Technical_Name",
                    Technical_Name
            );

            // put the spare parts name in the List
            String Data_arr[];
            ResultSet rs;
            //Get The count
            rs = statement.executeQuery("select count(spare_parts_name) from spare_parts");
            rs.next();
            int var_count = rs.getInt(1);
            Data_arr = new String[var_count];
            //Get the data
            rs = statement.executeQuery("select spare_parts_name from spare_parts");
            int index = 0;
            while (rs.next()) {
                Data_arr[index] = rs.getString("spare_parts_name");
                index++;
            }
            Spare_Parts_List.setModel(new javax.swing.DefaultComboBoxModel(Data_arr));

            new Get_TABLE_Data("select * from TECHNICAL_DETAIL_VIEW Where condition_state = 'Available' ORDER BY id asc",
                    Table);

        } catch (SQLException ex) {
            Logger.getLogger(Technical_MaintenanceInformation.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowOpened

    private void TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableMouseClicked
        // Get the selected row index in the table
        int selectedRow = Table.getSelectedRow();

        // Put the selected row index from the view to the model
        int modelRow = Table.convertRowIndexToModel(selectedRow);

        // Get the DefaultTableModel
        DefaultTableModel tbmodel = (DefaultTableModel) Table.getModel();

        // Use the modelRow to get values from the correct row in the model
        Report_id_Text.setText(tbmodel.getValueAt(modelRow, 0).toString());
        Floor.setText(tbmodel.getValueAt(modelRow, 3).toString());
        Office.setText(tbmodel.getValueAt(modelRow, 4).toString());
    }//GEN-LAST:event_TableMouseClicked

    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        if (Spare_Parts_List.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "No item selected.");
        } else {
            //Put the Data in the Total List
            ListModel.addElement(Spare_Parts_List.getSelectedValue());
            Spare_Parts_List_Total.setModel(ListModel);
            //Add the total
            int Cost_Selection = Integer.parseInt(Cost.getText());
            TotalCost = TotalCost + Cost_Selection;
            Total.setText(TotalCost + "");

        }
    }//GEN-LAST:event_AddActionPerformed

    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        if (Spare_Parts_List_Total.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "No item selected.");
        } else {
            try {
                //Connection
                Connection c = Connection_fun.con();

                //Get the Cost With the Name Selection
                int Cost_Deleted = 0;
                String Selection = Spare_Parts_List_Total.getSelectedValue();
                PreparedStatement Cost_Statement = c.prepareStatement("select Cost from spare_parts where spare_parts_name=?");
                Cost_Statement.setObject(1, Selection);
                ResultSet Cost_result = Cost_Statement.executeQuery();
                if (Cost_result.next()) {
                    Cost_Deleted = Integer.parseInt(Cost_result.getString("Cost"));
                } else {
                    JOptionPane.showMessageDialog(this, "Spare Part not found.");
                }

                //Delete the Selection and - the Cost Deleted
                ListModel.removeElementAt(Spare_Parts_List_Total.getSelectedIndex());
                TotalCost = TotalCost - Cost_Deleted;
                Total.setText(TotalCost + "");
            } catch (SQLException ex) {
                Logger.getLogger(Technical_MaintenanceInformation.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_DeleteActionPerformed


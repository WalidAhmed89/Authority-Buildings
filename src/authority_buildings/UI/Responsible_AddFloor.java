    private void TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableMouseClicked
        try {
            // Get the selected row index in the table
            int selectedRow = Table.getSelectedRow();

            // Put the selected row index from the view to the model
            int modelRow = Table.convertRowIndexToModel(selectedRow);

            // Get the Value in the row and column selection
            Role_Selection = Table.getModel().getValueAt(modelRow, 0).toString();
            Floor_Selection = Table.getModel().getValueAt(modelRow, 1).toString();

            // Put the selection in the id_Selection_Delete variable
            Connection c = Connection_fun.con();
            PreparedStatement ID_Statement = c.prepareStatement("SELECT location_role, Location_office_number FROM Location_Employee_View WHERE location_role = ? AND Location_office_number = ?");
            ID_Statement.setString(1, Role_Selection);
            ID_Statement.setString(2, Floor_Selection);
            ResultSet Test = ID_Statement.executeQuery();

            if (Test.next()) {
                Role_Delete = Test.getString("location_role");
                Office_Delete = Test.getString("Location_office_number");
            }

            // Set the data to the corresponding text fields
            Floor.setText(Role_Selection);
            Office.setText(Floor_Selection);

        } catch (SQLException ex) {
            Logger.getLogger(Responsible_Add_Delete.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_TableMouseClicked

    private void Update_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Update_ButtonActionPerformed
        try {
            // Connection
            Connection c = Connection_fun.con();
            Statement statement = c.createStatement();

            // Get the location selection
            String Location_id = null;
            PreparedStatement Location_id_statement = c.prepareStatement("SELECT id FROM location WHERE role = ? AND office_number = ?");
            Location_id_statement.setString(1, Role_Selection);
            Location_id_statement.setString(2, Floor_Selection);
            ResultSet Location_id_result = Location_id_statement.executeQuery();

            if (Location_id_result.next()) {
                Location_id = Location_id_result.getString("id");
            } else {
                JOptionPane.showMessageDialog(this, "Location not found.");
            }
            //The Insert into the Location Table
            PreparedStatement CheckLocation = c.prepareCall("SELECT COUNT(*) AS count FROM Location WHERE role=? and office_number=?");
            CheckLocation.setString(1, Floor.getText());
            CheckLocation.setString(2, Office.getText());
            ResultSet CheckLocationResult = CheckLocation.executeQuery();
            
            //If the location is existing
            if (CheckLocationResult.next() && CheckLocationResult.getInt("count") > 0) {
                JOptionPane.showMessageDialog(this, "Location is already exists");
            } else {
                
                // Update the location
                PreparedStatement Update_Location = c.prepareStatement("UPDATE Location SET role = ?, office_number = ? WHERE id = ?");
                Update_Location.setString(1, Floor.getText());
                Update_Location.setString(2, Office.getText());
                Update_Location.setString(3, Location_id);

                int updateCount = Update_Location.executeUpdate();

                if (updateCount >= 1) {
                    JOptionPane.showMessageDialog(this, "Update Successful");
                    Floor.setText("");
                    Office.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "We have an issue updating the location.");
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Responsible_Add_Delete.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_Update_ButtonActionPerformed


    private void Add_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Add_ButtonActionPerformed
        try {
            //Connection
            Connection c = Connection_fun.con();

            //The Insert into the Location Table
            PreparedStatement CheckLocation = c.prepareCall("SELECT COUNT(*) AS count FROM Location WHERE role=? and office_number=?");
            CheckLocation.setString(1, Floor.getText());
            CheckLocation.setString(2, Office.getText());
            ResultSet CheckLocationResult = CheckLocation.executeQuery();

            //If the location is existing
            if (CheckLocationResult.next() && CheckLocationResult.getInt("count") > 0) {
                JOptionPane.showMessageDialog(this, "Location is already exists");
            } else {
                PreparedStatement Add_Floor_Office_Statement = c.prepareStatement("INSERT INTO location VALUES (ADD_FLOOR_SEQ.nextval,?,?)");
                Add_Floor_Office_Statement.setString(1, Floor.getText());
                Add_Floor_Office_Statement.setString(2, Office.getText());
                int updateCount = Add_Floor_Office_Statement.executeUpdate();

                if (updateCount >= 1) {
                    JOptionPane.showMessageDialog(this, "Success");
                } else {
                    JOptionPane.showMessageDialog(this, "We Have a issue ");
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Responsible_AddFloor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_Add_ButtonActionPerformed


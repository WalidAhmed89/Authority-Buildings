    private void Register_Report_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Register_Report_ButtonActionPerformed
        try {
            //the connection
            Connection c = Connection_fun.con();

            // Retrieve Location ID
            String Location_id = null;
            PreparedStatement Location_id_statement = c.prepareStatement("SELECT id FROM location WHERE role = ? AND office_number = ?");
            Location_id_statement.setObject(1, Floor.getText());
            Location_id_statement.setObject(2, Office.getText());
            ResultSet Location_id_result = Location_id_statement.executeQuery();

            if (Location_id_result.next()) {
                Location_id = Location_id_result.getString("id");
            } else {
                JOptionPane.showMessageDialog(this, "Location not found.");
            }

            // Retrieve Type of Malfunction ID
            String Type_of_Maflucation_id = null;
            PreparedStatement Type_of_Maflucation_id_statement = c.prepareStatement("SELECT id FROM type_of_malfuncition WHERE type_of_malfuncition = ?");
            Type_of_Maflucation_id_statement.setObject(1, Type_Malfunction.getSelectedItem());
            ResultSet Type_of_Maflucation_result = Type_of_Maflucation_id_statement.executeQuery();

            if (Type_of_Maflucation_result.next()) {
                Type_of_Maflucation_id = Type_of_Maflucation_result.getString("id");
            } else {
                JOptionPane.showMessageDialog(this, "Type of malfunction not found.");
            }

            // Insert the data into REPORTS table 
            PreparedStatement Reports_statement = c.prepareStatement("INSERT INTO REPORTS(ID, STARTDATE, TYPE_OF_MALFUNCITION_ID,Employee_id, LOCATION_ID,Description,Condition_id) VALUES (REPORTS_SEQ.nextval,sysdate,?,?,?,?,1)");
            Reports_statement.setString(1, Type_of_Maflucation_id);
            Reports_statement.setString(2, Employee_id);
            Reports_statement.setString(3, Location_id);
            Reports_statement.setString(4, Description.getText());

            int updateCount = Reports_statement.executeUpdate();

            if (updateCount >= 1) {
                JOptionPane.showMessageDialog(this, "Success");
                Description.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "We Have a issue ");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "SQL Error:" + ex.getMessage());
        }

    }//GEN-LAST:event_Register_Report_ButtonActionPerformed


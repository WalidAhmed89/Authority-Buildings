    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            // Connection and the Statement
            Connection c = Connection_fun.con();
            Statement statement = c.createStatement();

            // Retrieve Location (Role,Office) in the Text
            //First get the Location_id With the Employee_id
            String Location_id = null;
            PreparedStatement Location_id_statement = c.prepareStatement("SELECT Location_id FROM EMPLOYEESPOSITION WHERE ID=?");
            Location_id_statement.setObject(1, Employee_id);
            ResultSet Location_id_ResultSet = Location_id_statement.executeQuery();

            if (Location_id_ResultSet.next()) {
                Location_id = Location_id_ResultSet.getString("Location_id");
            } else {
                JOptionPane.showMessageDialog(this, "Location not found for the employee.");
            }

            //2nd get the (Role,Office) with the Location_id
            if (Location_id != null) {
                PreparedStatement Location_Statement = c.prepareStatement("SELECT Role, Office_number FROM Location WHERE ID=?");
                Location_Statement.setString(1, Location_id);
                ResultSet Location_ResultSet = Location_Statement.executeQuery();

                if (Location_ResultSet.next()) {
                    // 3nd Set (Role,Office) in Text
                    Floor.setText(Location_ResultSet.getString("Role"));
                    Office.setText(Location_ResultSet.getString("Office_number"));
                } else {
                    JOptionPane.showMessageDialog(this, "No location details found.");
                }
            }
            //Put the Register_Report Table data with the id
            // Table Code
            DefaultTableModel TableModels = (DefaultTableModel) Table.getModel();
            TableModels.setRowCount(0);  // Clear the table

            // Execute the query and retrieve the result set
            PreparedStatement Reports_Table = c.prepareStatement("select report_id,employee_first_name,employee_last_name,malfunction_type,location_role,location_office_number,report_description,condition_status from Register_Report Where Employee_id =? and Condition_Status = 'Available' ");
            Reports_Table.setString(1, Employee_id);
            ResultSet Result = Reports_Table.executeQuery();
            ResultSetMetaData metaData = Result.getMetaData();
            int columnCount = metaData.getColumnCount();
            Object[] Array_Tabel;

            // Create a Array and Put The Table Data on it.
            while (Result.next()) {

                Array_Tabel = new Object[columnCount];

                for (int i = 0; i < columnCount; i++) {
                    // Retrieve the object and check if it is NULL
                    Object value = Result.getObject(i + 1);

                    // If the value is NULL
                    Array_Tabel[i] = (value != null) ? value : "N/A";
                }

                // Add the row to the table model
                TableModels.addRow(Array_Tabel);
            }

            //Put All TYPE_OF_MALFUNCITION data to the ComboBox using the Get_SQL_Data_ComboBox Function
            new Get_SQL_Data_ComboBox("select count(*) from TYPE_OF_MALFUNCITION", "select type_of_malfuncition from TYPE_OF_MALFUNCITION", "type_of_malfuncition", Type_Malfunction);
        } catch (SQLException ex) {
            Logger.getLogger(Employee_RegisterReport.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowOpened

    private void TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableMouseClicked
        // Get the selected row index in the table
        int selectedRow = Table.getSelectedRow();

        // Put the selected row index from the view to the model
        int modelRow = Table.convertRowIndexToModel(selectedRow);

        // Get the DefaultTableModel
        DefaultTableModel tbmodel = (DefaultTableModel) Table.getModel();

        // Use the modelRow to get the data from the correct row in the model
        Report_id_Selection = tbmodel.getValueAt(modelRow, 0).toString();
        String Type_of_Malfunction = tbmodel.getValueAt(modelRow, 3).toString();
        String Role = tbmodel.getValueAt(modelRow, 4).toString();
        String Office = tbmodel.getValueAt(modelRow, 5).toString();
        String Description = tbmodel.getValueAt(modelRow, 6).toString();
        Condition = tbmodel.getValueAt(modelRow, 7).toString();

        // Put the data in the ComboBox and Text fields
        Type_Malfunction.setSelectedItem(Type_of_Malfunction);
        Floor.setText(Role);
        this.Office.setText(Office);
        this.Description.setText(Description);
    }//GEN-LAST:event_TableMouseClicked

